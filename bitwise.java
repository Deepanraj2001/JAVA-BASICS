import java.io.*;
import java.lang.*;
import java.util.*;
 
class bitwise
{
 public static void main(String args[])
 {
  int x=10;
  int y=20;
  int z=30;
  x=x|40;
  y>>=1;
  z<<=2;
  System.out.println("x|40 :"+x);
  System.out.println("y>>1 :"+y);
  System.out.println("z<<2 :"+z);
  
  x=10;
  z=30;
  x=x^z;
  System.out.println("x^z:"+z);
  }
}     
     
    
      
     
     
     
     
     
   
      