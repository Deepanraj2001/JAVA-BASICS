import java.io.*;
import java.lang.*;
import java.util.*;
 
//USING (DO WHILE) TO PRINT A STATEMENT RECURSIVLY

class dowhiledemo
{
 public static void main(String args[])
 {
  int i=1;
  do
  {
   System.out.println("LOOP ROUND : "+i);  
   i++;
  }while(i<=10);
 }
} 





